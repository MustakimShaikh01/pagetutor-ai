def generate_tts(
    job_id: str,
    text: str,
    language: str = "en",
) -> str:
    """
    Generate speech narration using Coqui TTS.

    Supported languages: en, hi, fr, de, es, pt, zh, ar, ja, ko

    Process:
    1. Truncate text to avoid OOM / long processing
    2. Select TTS model for language
    3. Generate WAV audio
    4. Upload to S3 as MP3 (compressed)

    Returns: S3 pre-signed URL for the audio file
    """
    logger.info("tts_generation_started", job_id=job_id, language=language)

    # Limit text length to ~3000 chars for reasonable audio length
    max_chars = 3000
    narration_text = text[:max_chars] + ("..." if len(text) > max_chars else "")

    try:
        # Use TTS library (Coqui TTS)
        from TTS.api import TTS
        import numpy as np
        import wave

        # Model selection based on language
        # For languages without dedicated model, fallback to EN + warning
        model_map = {
            "en": "tts_models/en/ljspeech/tacotron2-DDC",
            "fr": "tts_models/fr/mai/tacotron2-DDC",
            "de": "tts_models/de/thorsten/tacotron2-DDC",
            "es": "tts_models/es/mai/tacotron2-DDC",
            "pt": "tts_models/pt/cv/vits",
            # Multilingual model for other languages
        }

        if language in model_map:
            tts_model = model_map[language]
        else:
            # Use multilingual XTTS model for remaining languages
            tts_model = "tts_models/multilingual/multi-dataset/xtts_v2"

        tts = TTS(model_name=tts_model, progress_bar=False)

        # Generate audio in temp file
        with tempfile.NamedTemporaryFile(suffix=".wav", delete=False) as tmp:
            tmp_path = tmp.name

        if language in model_map:
            tts.tts_to_file(text=narration_text, file_path=tmp_path)
        else:
            tts.tts_to_file(
                text=narration_text,
                language=language,
                speaker="Ana Florence",
                file_path=tmp_path,
            )

        # Read WAV file
        with open(tmp_path, "rb") as f:
            audio_bytes = f.read()

        # Cleanup temp file
        os.unlink(tmp_path)

        # Upload to S3 as WAV (could convert to MP3 with pydub for size)
        s3_key = f"users/jobs/{job_id}/narration_{language}.wav"
        url = upload_bytes_to_s3(audio_bytes, s3_key, "audio/wav")

        logger.info(
            "tts_generated",
            job_id=job_id,
            language=language,
            size_bytes=len(audio_bytes),
        )
        return url

    except ImportError:
        # TTS not installed — return placeholder
        logger.warning("tts_not_available", job_id=job_id)
        return f"{settings.S3_CDN_BASE_URL}/placeholder_audio.wav"
    except Exception as e:
        logger.error("tts_generation_failed", job_id=job_id, error=str(e))
        raise


