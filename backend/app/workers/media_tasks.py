# PPT GENERATION
# ===========================================================
def generate_ppt(
    job_id: str,
    document_id: str,
    segments: List[Dict],
    summary: str,
) -> str:
    """
    Generate a PowerPoint presentation from document segments.

    Structure:
    - Slide 1: Title + Overview
    - Slides 2..N: One slide per segment (title + bullet points)
    - Last slide: Summary

    Returns: S3 pre-signed URL for the .pptx file
    """
    logger.info("ppt_generation_started", job_id=job_id)

    try:
        from pptx import Presentation
        from pptx.util import Inches, Pt, Emu
        from pptx.dml.color import RGBColor
        from pptx.enum.text import PP_ALIGN

        prs = Presentation()
        prs.slide_width = Inches(13.33)
        prs.slide_height = Inches(7.5)

        # --------------------------------------------------
        # Slide 1: Title Slide
        # --------------------------------------------------
        title_layout = prs.slide_layouts[0]
        slide = prs.slides.add_slide(title_layout)
        title = slide.shapes.title
        subtitle = slide.placeholders[1]

        title.text = "Document Summary"
        title.text_frame.paragraphs[0].font.color.rgb = RGBColor(0x1A, 0x1A, 0x2E)
        title.text_frame.paragraphs[0].font.size = Pt(40)

        subtitle.text = (
            f"Generated by PageTutor AI\n"
            f"github.com/MustakimShaikh01"
        )

        # --------------------------------------------------
        # Overview slide (summary truncated)
        # --------------------------------------------------
        content_layout = prs.slide_layouts[1]
        overview_slide = prs.slides.add_slide(content_layout)
        overview_slide.shapes.title.text = "Document Overview"
        tf = overview_slide.placeholders[1].text_frame
        tf.text = summary[:600] + ("..." if len(summary) > 600 else "")
        tf.word_wrap = True

        # --------------------------------------------------
        # One slide per segment
        # --------------------------------------------------
        for i, segment in enumerate(segments[:15]):  # Max 15 content slides
            seg_slide = prs.slides.add_slide(content_layout)
            seg_slide.shapes.title.text = segment.get("title", f"Section {i+1}")

            tf = seg_slide.placeholders[1].text_frame
            tf.text = segment.get("description", "")

            key_points = segment.get("key_points", [])
            for pt in key_points[:5]:  # Max 5 bullet points
                p = tf.add_paragraph()
                p.text = f"• {pt}"
                p.font.size = Pt(18)
                p.level = 1

        # --------------------------------------------------
        # Save to bytes
        # --------------------------------------------------
        ppt_buffer = io.BytesIO()
        prs.save(ppt_buffer)
        ppt_bytes = ppt_buffer.getvalue()

        # Upload to S3
        s3_key = f"users/jobs/{job_id}/presentation.pptx"
        url = upload_bytes_to_s3(
            ppt_bytes,
            s3_key,
            "application/vnd.openxmlformats-officedocument.presentationml.presentation",
        )

        logger.info(
            "ppt_generated",
            job_id=job_id,
            slides=len(prs.slides),
            size_bytes=len(ppt_bytes),
        )
        return url

    except Exception as e:
        logger.error("ppt_generation_failed", job_id=job_id, error=str(e))
        raise


